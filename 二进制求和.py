"""
int(a, 2) 是 Python 中 int 函数的一种用法，用于将字符串 a 转换为一个整数。这个函数有两个参数：

第一个参数是要转换的字符串。在这种情况下，a 是一个二进制数表示的字符串，比如 "1010"。

第二个参数是基数（或称为进制）。这告诉函数 a 使用的是哪种数制。在这里，基数是 2，表示 a 是一个二进制数。这意味着字符串中的每个字符都应该是 0 或 1。

函数 int(a, 2) 的作用是读取二进制字符串 a 并将其转换为对应的十进制整数。例如：

当 a = "11" 时，int(a, 2) 会将 "11" 转换为二进制的 11，也就是十进制的 3。
当 a = "1010" 时，int(a, 2) 会将 "1010" 转换为二进制的 1010，也就是十进制的 10。

"""
class Solution:
    def addBinary(a: str, b: str) -> str:
      # 将二进制字符串转换为整数
      num_a = int(a, 2)
      num_b = int(b, 2)
  
      # 计算两个整数的和
      sum_num = num_a + num_b
  
      # 将和转换回二进制字符串，并去掉前缀'0b'
      return bin(sum_num)[2:]

# 测试函数
example1 = addBinary("11", "1")
example2 = addBinary("1010", "1011")

