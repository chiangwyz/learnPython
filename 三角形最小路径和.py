"""
定一个三角形 triangle ，找出自顶向下的最小路径和。
每一步只能移动到下一行中相邻的结点上。
相邻的结点 在这里指的是 下标 与 上一层结点下标 相同或者等于 上一层结点下标 + 1 的两个结点。
也就是说，如果正位于当前行的下标 i ，那么下一步可以移动到下一行的下标 i 或 i + 1 。

示例 1：
输入：triangle = [[2],[3,4],[6,5,7],[4,1,8,3]]
输出：11
解释：如下面简图所示：
   2
  3 4
 6 5 7
4 1 8 3
自顶向下的最小路径和为 11（即，2 + 3 + 5 + 1 = 11）。

示例 2：
输入：triangle = [[-10]]
输出：-10

为了解决这个问题，我们可以使用动态规划。思路是自底向上地计算每一层的最小路径和，直到达到顶部。
这种方式可以避免重复计算，并且不需要额外的空间来存储中间结果。具体步骤如下：

1. 从三角形的倒数第二层开始，对于每一层的每一个元素，计算通过这个元素到达底层的最小路径和。
  这个最小路径和等于当前元素的值加上它下一层的两个相邻元素的最小路径和。

2. 重复上述步骤，直到计算到三角形的顶部。

3. 三角形顶部的元素值就是最小路径和。

下面是这个算法的 Python 实现：
"""

class Solution:
    def minimumTotal(self, triangle: list[list[int]]) -> int:
        n = len(triangle)
        # 从三角形的倒数第二层开始向上计算
        for i in range(n - 2, -1, -1):
            for j in range(len(triangle[i])):
                # 更新当前位置的最小路径和
                triangle[i][j] += min(triangle[i + 1][j], triangle[i + 1][j + 1])
        return triangle[0][0]




